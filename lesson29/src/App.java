public class App {
    public static void main(String[] args) {
        char grade = 'B';

        if(grade == 'A')
            System.out.println("You earned an A. Fantastic job!"); 

        else if(grade == 'B')
            System.out.println("You earned an B. Good job!");

        else if(grade == 'C')
            System.out.println("You earned an C. Ok job!");
        
        else if(grade == 'D')
            System.out.println("You earned an D. You did poorly!");
        
        else
            System.out.println("You earned an F. You failed!");
        }
            
    }
