public class App {
    /** 
     * Addition ==> +
     * Subtraction ==> -
     * Multiplication ==> *
     * Division ==> /
     * 
    */
    public static void main(String [] args) {
        int num1, num2, num3, num4;

        int sum, difference;
        int multiply, divide;

        num1 = 10;
        num2 = 3;
        num3 = 5;
        num4 = 2;

        sum = num1 + 5;
        difference = num3 - num2;
        multiply = num3 * num4;
        divide = num1 / num4;



        System.out.println(multiply);
        System.out.println(sum);
        System.out.println(difference);
        System.out.println(divide);
    }
}